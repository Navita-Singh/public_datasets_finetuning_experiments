import torch
import pandas as pd
from transformers import AutoTokenizer, AutoModelForCausalLM

# =========================
# 2. LOAD MODEL
# =========================

model_name = "Qwen/Qwen2.5-7B"

tokenizer = AutoTokenizer.from_pretrained(
    model_name,
    trust_remote_code=True
)

model = AutoModelForCausalLM.from_pretrained(
    model_name,
    torch_dtype=torch.float16,
    device_map="auto",
    trust_remote_code=True
)

model.eval()

# =========================
# 3. GENERATION FUNCTION
# =========================

def generate_response(
    prompt,
    system_prompt=None,
    temperature=0.7,
    top_p=0.9,
    max_tokens=256
):
    messages = []

    if system_prompt:
        messages.append({"role": "system", "content": system_prompt})

    messages.append({"role": "user", "content": prompt})

    text = tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True
    )

    inputs = tokenizer(text, return_tensors="pt").to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_tokens,
            temperature=temperature,
            top_p=top_p,
            do_sample=True
        )

    decoded = tokenizer.decode(outputs[0], skip_special_tokens=True)

    # Extract assistant response
    if "assistant" in decoded:
        return decoded.split("assistant")[-1].strip()
    return decoded


# =========================
# 4. ABLATION CONFIGS
# =========================

ablation_configs = [
    {
        "name": "baseline",
        "system_prompt": None,
        "temperature": 0.7,
        "top_p": 0.9
    },
    {
        "name": "low_temperature",
        "system_prompt": None,
        "temperature": 0.2,
        "top_p": 0.9
    },
    {
        "name": "high_temperature",
        "system_prompt": None,
        "temperature": 1.0,
        "top_p": 0.9
    },
    {
        "name": "strict_system",
        "system_prompt": "You are a precise assistant. Answer in bullet points.",
        "temperature": 0.7,
        "top_p": 0.9
    },
    {
        "name": "creative_system",
        "system_prompt": "You are a creative storyteller.",
        "temperature": 0.9,
        "top_p": 0.95
    }
]


# =========================
# 5. OPTIONAL FEW-SHOT
# =========================

def build_few_shot(prompt):
    return f"""
Q: What is overfitting?
A: Overfitting is when a model learns noise in training data and performs poorly on unseen data.

Q: What is underfitting?
A: Underfitting is when a model is too simple to capture patterns in data.

Q: {prompt}
A:
"""


# =========================
# 6. RUN ABLATIONS
# =========================

def run_ablation(prompt, configs, use_few_shot=False):
    results = []

    for cfg in configs:
        final_prompt = build_few_shot(prompt) if use_few_shot else prompt

        print(f"\n=== Running: {cfg['name']} ===")

        output = generate_response(
            prompt=final_prompt,
            system_prompt=cfg["system_prompt"],
            temperature=cfg["temperature"],
            top_p=cfg["top_p"]
        )

        results.append({
            "config": cfg["name"],
            "output": output,
            "length": len(output.split())
        })

        print(output[:300], "\n")

    return results


# =========================
# 7. SIMPLE EVALUATION METRIC
# =========================

def evaluate_results(results):
    df = pd.DataFrame(results)

    summary = df.groupby("config").agg(
        avg_length=("length", "mean")
    ).reset_index()

    return df, summary


# =========================
# 8. RUN EXPERIMENT
# =========================

if __name__ == "__main__":

    prompt = "Explain the difference between overfitting and underfitting."

    # zero-shot ablation
    results = run_ablation(prompt, ablation_configs, use_few_shot=False)

    # few-shot ablation (optional)
    results_fewshot = run_ablation(prompt, ablation_configs, use_few_shot=True)

    # evaluate
    df, summary = evaluate_results(results)

    print("\n=== FULL RESULTS ===")
    print(df)

    print("\n=== SUMMARY ===")
    print(summary)

    # save results
    df.to_csv("ablation_results.csv", index=False)
    summary.to_csv("ablation_summary.csv", index=False)
# =========================
# 1. Imports
# =========================
import torch
import torch.nn.functional as F
from transformers import AutoTokenizer, AutoModelForCausalLM
from datasets import Dataset

device = "cuda" if torch.cuda.is_available() else "cpu"

# =========================
# 2. Load Model
# =========================
model_name = "gpt2"

tokenizer = AutoTokenizer.from_pretrained(model_name)
tokenizer.pad_token = tokenizer.eos_token

policy_model = AutoModelForCausalLM.from_pretrained(model_name).to(device)
ref_model = AutoModelForCausalLM.from_pretrained(model_name).to(device)

# Freeze reference model
for param in ref_model.parameters():
    param.requires_grad = False

# =========================
# 3. Preference Dataset
# =========================
data = [
    {
        "prompt": "What is AI?",
        "chosen": "AI is the simulation of human intelligence by machines.",
        "rejected": "AI is random stuff."
    },
    {
        "prompt": "Explain gravity",
        "chosen": "Gravity is a force that attracts objects with mass.",
        "rejected": "Gravity is magic."
    },
    {
        "prompt": "What is machine learning?",
        "chosen": "Machine learning enables systems to learn from data.",
        "rejected": "Machine learning is just computers doing things."
    }
]

dataset = Dataset.from_list(data)

# =========================
# 4. Helper: Log Probabilities
# =========================
def get_log_probs(model, input_ids, attention_mask):
    outputs = model(input_ids=input_ids, attention_mask=attention_mask)
    logits = outputs.logits[:, :-1, :]
    labels = input_ids[:, 1:]

    log_probs = F.log_softmax(logits, dim=-1)
    selected = log_probs.gather(2, labels.unsqueeze(-1)).squeeze(-1)

    return selected.sum(dim=1)

# =========================
# 5. DPO Loss
# =========================
def dpo_loss(policy_chosen_logp, policy_rejected_logp,
             ref_chosen_logp, ref_rejected_logp,
             beta=0.1):

    policy_diff = policy_chosen_logp - policy_rejected_logp
    ref_diff = ref_chosen_logp - ref_rejected_logp

    logits = beta * (policy_diff - ref_diff)

    return -F.logsigmoid(logits).mean()

# =========================
# 6. Training Setup
# =========================
optimizer = torch.optim.AdamW(policy_model.parameters(), lr=1e-5)

def tokenize_pair(prompt, response):
    text = prompt + " " + response
    return tokenizer(
        text,
        return_tensors="pt",
        padding=True,
        truncation=True
    )

# =========================
# 7. Training Loop
# =========================
print("Starting DPO training...")

for epoch in range(3):
    total_loss = 0

    for example in dataset:
        chosen = tokenize_pair(example["prompt"], example["chosen"])
        rejected = tokenize_pair(example["prompt"], example["rejected"])

        # Move to device
        chosen = {k: v.to(device) for k, v in chosen.items()}
        rejected = {k: v.to(device) for k, v in rejected.items()}

        # Policy log probs
        policy_chosen_logp = get_log_probs(policy_model, **chosen)
        policy_rejected_logp = get_log_probs(policy_model, **rejected)

        # Reference log probs
        with torch.no_grad():
            ref_chosen_logp = get_log_probs(ref_model, **chosen)
            ref_rejected_logp = get_log_probs(ref_model, **rejected)

        # Compute loss
        loss = dpo_loss(
            policy_chosen_logp,
            policy_rejected_logp,
            ref_chosen_logp,
            ref_rejected_logp
        )

        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    print(f"Epoch {epoch+1}, Loss: {total_loss:.4f}")

# =========================
# 8. Test Model
# =========================
print("\nTesting fine-tuned model...")

def generate(prompt):
    inputs = tokenizer(prompt, return_tensors="pt").to(device)
    output = policy_model.generate(
        **inputs,
        max_new_tokens=40,
        pad_token_id=tokenizer.eos_token_id
    )
    return tokenizer.decode(output[0], skip_special_tokens=True)

print(generate("What is artificial intelligence?"))
print(generate("Explain machine learning"))
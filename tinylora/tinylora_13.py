import torch
from transformers import AutoModelForCausalLM
from peft import LoraConfig, get_peft_model

model_id = "Qwen/Qwen2.5-7B"

# 1. Load Qwen 2.5
model = AutoModelForCausalLM.from_pretrained(
    model_id,
    torch_dtype=torch.bfloat16,
    device_map="auto"
)

# 2. Define the LoRA config
# To get close to the "Tiny" architecture's behavior:
# We use r=1 (lowest rank) and target only one specific tiny dimension.
config = LoraConfig(
    r=1,
    lora_alpha=1,
    target_modules=["q_proj"], # Target only one module type to keep params low
    lora_dropout=0.0,
    bias="none",
    task_type="CAUSAL_LM",
)

model = get_peft_model(model, config)

# 3. The "Manual 13" Hack:
# We freeze all LoRA parameters except for a small slice of 13 values.
trainable_count = 0
for name, param in model.named_parameters():
    if "lora_" in name:
        param.requires_grad = False # Freeze standard LoRA

# Create the 13-parameter "Reasoning Vector"
reasoning_vector = torch.nn.Parameter(torch.randn(13))
model.register_parameter("tiny_lora_vector", reasoning_vector)

# Now only these 13 parameters will update
print(f"Trainable parameters: {sum(p.numel() for p in model.parameters() if p.requires_grad)}")